package LearnJavapkg1;

public class PrimtiveDatatypesClass2 {
    public static void main(String[] args) {
        byte number1byte=127;
        short number2short=150;
        int number3int=1233333333;
        long number4long=12333333333L;//L or l in end is mandatary
        float number5float=15.6F;//F or f is mandatory
        double number6double=15.66666666;
        char letter1char='j';
        boolean true1bool=true;
        // println:line by line, print:print next to next
        System.out.println(number1byte);
        System.out.println(number2short);
        System.out.println(number3int);
        System.out.println(number4long);
        System.out.println(number4long);
        System.out.println(number5float);
        System.out.println(number6double);
        System.out.println(letter1char);
        System.out.println(true1bool);
    }
}
